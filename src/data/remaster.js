export function itemRename(itemName) {
  if (foundry.utils.isNewerVersion(game.version, "5.9.0") && game.modules.get("pf2e-legacy-content")?.active) return itemName;
  const remasterName = REMASTER_NAMES.find((remaster) => remaster.hlName === itemName);
  if (remasterName) {
    return remasterName.foundryName;
  }
  return itemName;
}